"""
Minimal local example for Bindu.

This script helps first-time users verify that
the repository runs locally without using
cookiecutter or production setup.
"""

def main():
    user_input = "Hello Bindu"
    output = {
        "input": user_input,
        "message": "Bindu local example ran successfully"
    }
    print(output)

if __name__ == "__main__":
    main()
